package tarea4;

public class Ejerc1App {

	public static void main(String[] args) {
		
		//Variables
		int a = 2, b = 3;
		
		//Salida por pantalla
		System.out.println("Suma de " + a + " y " + b + " es igual a " + (a + b));
		System.out.println("Resta de " + a + " menos " + b + " es igual a " + (a - b));
		System.out.println("Multiplicación de " + a + " por " + b + " es igual a " + (a * b));
		System.out.println("División de " + a + " entre " + b + " es igual a " + ((float)a / (float)b));
		System.out.println("Módulo de " + a + " entre " + b + " es igual a " + (a % b));
		
	}

}
